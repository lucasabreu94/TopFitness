    private void jBtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSalvarActionPerformed
       
        if (jTxtNomeExercicio.getText() == "" || jTAreaDescricao.getText() == "" || jCbxGrupoMuscular.getSelectedItem() != "SELECIONAR" ){
        
        Exercicio e = new Exercicio();
        ExercicioDAO dao = new ExercicioDAO();

        e.setNome(jTxtNomeExercicio.getText());
        e.setDescricao(jTAreaDescricao.getText());
        e.setCategoria((String)jCbxGrupoMuscular.getSelectedItem());
        
       
        dao.create(e);

        //Limpa os campos apos gravação
        jTxtNomeExercicio.setText("");
        jTAreaDescricao.setText("");
        jCbxGrupoMuscular.setSelectedItem("SELECIONAR");
        
        
        }
        else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        }
        
    }//GEN-LAST:event_jBtnSalvarActionPerformed

    
