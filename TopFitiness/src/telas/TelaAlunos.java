    public void readJTblAluno(){
        DefaultTableModel modelo = (DefaultTableModel) jTblAlunos.getModel();
        modelo.setNumRows(0);
        AlunoDAO adao = new AlunoDAO();
        
        for(Aluno a: adao.read()){     
            modelo.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getSexo(),
                a.getDatanasc(),
                a.getObjetivo()
            });
        }
    }
    
