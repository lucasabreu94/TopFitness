    private void jTblAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblAlunosMouseClicked
        int linha = jTblAlunos.getSelectedRow();

        /* Verifica se nÃ£o foi clicado em uma tabela vazia */
        if (linha > -1) {
            /* Captura o modelo da tabela */
            DefaultTableModel modelo = (DefaultTableModel) jTblAlunos.getModel();

            /* Copia os dados do registro selecionado para os campos texto */
            jTxtIdAluno.setText(modelo.getValueAt(linha, 0).toString());
            jTxtNome.setText(modelo.getValueAt(linha, 1).toString());

        }
    }//GEN-LAST:event_jTblAlunosMouseClicked

